class control_statements {
    public static void main(String args[]) {
        /*
        initiating variables and assigning values
         */
        int a, b;
        a = 100;
        b = 200;

        /*
        the if clause
         */
        if (a > b) {
            System.out.println("A is greater");
        }
        else {
            System.out.println("B is greater");
        }
    }
}