class Loops{
    public static void main(String[] args) {
        // for loop
        System.out.println("-- for --");
        for(int i=0; i<10; i++){
            System.out.println(i);
        }
        System.out.println("-- while --");
        int x = 10;
        while(x > 5){
            System.out.println(x);
            x--;
        }
    }
}